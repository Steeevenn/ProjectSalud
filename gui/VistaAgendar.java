    private void botonagendarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonagendarMousePressed

        try {
            ConexionBD cn = new ConexionBD();
            Connection conexion = cn.conexion();
            AccesoDatos ac = new AccesoDatos();

            String nombresql = nombrefield.getText();
            String cedulasql = cedulafield1.getText();
            String especialistasql = (String) especialistacombobox.getSelectedItem();
            java.util.Date fechaUtil = datefield.getDate();
            java.sql.Date fechasql = new java.sql.Date(fechaUtil.getTime());

            String horaStr = (String) horacombobox.getSelectedItem();
            LocalTime localtime = LocalTime.parse(horaStr);
            Time horasql = Time.valueOf(localtime);

            String motivosql = motivofield1.getText();

            if (ac.validarCitaMedico(conexion, fechasql, horasql, especialistasql)) {
                 ac.guardarCitas(conexion, fechasql, horasql, motivosql, nombresql, cedulasql, especialistasql);
            JOptionPane.showMessageDialog(this, " Cita agendada para " + nombresql + " con el especialista: " + especialistasql + " El dia :" + fechasql + " Hora programada: " + horasql);
            conexion.close();
            }else{
            JOptionPane.showMessageDialog(null, "Fecha y Hora no disponible, intentelo de nuevo");}
           
        } catch (SQLException ex) {
            Logger.getLogger(VistaAgendar.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error al guardar la cita: " + ex.getMessage());

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(VistaAgendar.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_botonagendarMousePressed

