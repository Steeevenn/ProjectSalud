  private void llenarHoraComboBox() {
// borrar items predeterminados
      horacombobox.removeAllItems();

        // Llenar el ComboBox con horas disponibles, por ejemplo, de 8:00 AM a 5:00 PM
        for (int hour = 8; hour <= 17; hour++) {
            for (int minute = 0; minute <= 45; minute += 15) {
                LocalTime time = LocalTime.of(hour, minute);

                horacombobox.addItem(time.toString());
            }
        }
    }

    public void llenarEspecialistasComboBox() throws SQLException, ClassNotFoundException {
        AccesoDatos accesoDatos = new AccesoDatos();
        ArrayList<String> nombresEspecialistas = accesoDatos.obtenerNombresEspecialistas();

        String[] nombresArray = nombresEspecialistas.toArray(new String[0]);

        especialistacombobox.setModel(new DefaultComboBoxModel<>(nombresArray));
    }
