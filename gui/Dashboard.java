    public void initPanelModCita() throws SQLException, ClassNotFoundException {

        VistaModificar md = new VistaModificar();
        md.setSize(900,900);
        md.setLocation(0, 0);
        md.revalidate();
        md.repaint();
        content.removeAll();
        content.add(md,BorderLayout.CENTER);
        content.revalidate();
        content.repaint();

    }

    public void initPanelConsultar() {

        // Crear una instancia del JPanel VistaConsultar
        VistaConsultar vistaConsultar = new VistaConsultar();

        // Configurar el tamaño y la ubicación del JPanel VistaConsultar
        vistaConsultar.setSize(900,900);
        vistaConsultar.setLocation(0, 0);

        // Agregar el JPanel VistaConsultar al JPanel content
        content.removeAll();
        content.add(vistaConsultar, BorderLayout.CENTER);

        // Actualizar la interfaz de usuario del JPanel content
        content.revalidate();
        content.repaint();
    }

