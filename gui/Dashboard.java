    public void fechamodificada() {
        LocalDate fechaActual = LocalDate.now();
        int dia = fechaActual.getDayOfMonth();
        Month mes = fechaActual.getMonth();
        int año = fechaActual.getYear();
        String nombreMes = mes.getDisplayName(TextStyle.FULL, new Locale("es", "ES"));
        String nombreMesMayus = nombreMes.toUpperCase();

        String fecha01 = "HOY ES " + dia + " DE " + nombreMesMayus + " DEL " + año;
        fecha.putClientProperty("FlatLaf.style", "font: bold $h1.regular.font");
        fecha.setText(fecha01);
    }

